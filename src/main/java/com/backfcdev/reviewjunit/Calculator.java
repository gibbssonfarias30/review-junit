package com.backfcdev.reviewjunit;

public class Calculator {
    public int sum(int num01, int num02){
        return num01 + num02;
    }

    public int divide(int num01, int num02){
        return num01 / num02;
    }

    public int multiplication(int num01, int num02){
        return num01 * num02;
    }

    public int subtraction(int num01, int num02){
        return num01 - num02;
    }
}
